package com.example.man;

public class NoteInfo {
    public int id;
    public String image;
    public String title;
    public String date;
    public String emotion;

    public NoteInfo(int id, String image, String title, String date, String emotion) {
        this.id = id;
        this.image = image;
        this.title = title;
        this.date = date;
        this.emotion = emotion;
    }
}
